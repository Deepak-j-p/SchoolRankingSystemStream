package com.example.model;

public class StudentSubjectTopper {
	
	private int id;
	private String name;
	private String subjectTopperIn;
	private int subjectMarks;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getSubjectTopperIn() {
		return subjectTopperIn;
	}
	public void setSubjectTopperIn(String subjectTopperIn) {
		this.subjectTopperIn = subjectTopperIn;
	}
	public int getSubjectMarks() {
		return subjectMarks;
	}
	public void setSubjectMarks(int subjectMarks) {
		this.subjectMarks = subjectMarks;
	}

}
